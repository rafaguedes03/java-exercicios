public class Calculadora{
    public double Somar(double num0, double num1){
        return num0+num1;
    }
    public double Subtrair(double num0, double num1){
        return num0-num1;
    }
    public double Multiplicar(double num0, double num1){
        return num0*num1;
    }
    public double Dividir(double num0, double num1){
        return num0/num1;
    }
}